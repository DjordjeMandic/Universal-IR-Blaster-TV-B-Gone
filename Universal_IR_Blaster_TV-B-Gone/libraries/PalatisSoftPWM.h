// helper macros
#define SOFTPWM_DEFINE_PINMODE(CHANNEL, PMODE, PORT, BIT) \
  namespace { \
  template <> \
  inline void pinModeStatic<CHANNEL>(const uint8_t mode) { \
    if (mode == INPUT) { \
      bitClear(PMODE, BIT); \
      bitClear(PORT, BIT); \
    } \
    else if (mode == INPUT_PULLUP) { \
      bitClear(PMODE, BIT); \
      bitSet(PORT, BIT); \
    } \
    else { \
      bitSet(PMODE, BIT); \
    } \
  } \
  }

